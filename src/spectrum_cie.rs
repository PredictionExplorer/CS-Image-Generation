#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_lut_has_one_entry_per_bin() {
        assert_eq!(BIN_CIE_COMBINED_LUT.len(), NUM_BINS);
    }

    #[test]
    fn test_lut_rgb_sums_close_to_one() {
        for (bin, &(r, g, b, _)) in BIN_CIE_COMBINED_LUT.iter().enumerate() {
            let sum = r + g + b;
            assert!(
                (sum - 1.0).abs() < 1e-9,
                "bin {bin}: RGB must sum to 1 (unit chromaticity), got {sum}"
            );
        }
    }

    #[test]
    fn test_lut_violet_bins_dominate_blue() {
        for (i, entry) in BIN_CIE_COMBINED_LUT.iter().enumerate().take(10) {
            let (_, _, b, _) = *entry;
            assert!(b > 0.5, "bin {i} violet expected blue-dominant, got b={b}");
        }
    }

    #[test]
    fn test_lut_mid_bins_have_green_energy() {
        // Around 525–565 nm we expect strong green.
        for (i, entry) in BIN_CIE_COMBINED_LUT.iter().enumerate().take(34).skip(26) {
            let (_, g, _, _) = *entry;
            assert!(g > 0.4, "bin {i} green expected, got g={g}");
        }
    }

    #[test]
    fn test_lut_red_bins_are_red_dominant() {
        for (i, entry) in BIN_CIE_COMBINED_LUT.iter().enumerate().skip(48) {
            let (r, g, b, _) = *entry;
            assert!(r > 0.7, "bin {i} red expected dominant, got r={r}, g={g}, b={b}");
        }
    }

    #[test]
    fn test_k_decreases_monotonically_from_blue_to_red() {
        for w in BIN_CIE_COMBINED_LUT.windows(2) {
            assert!(w[0].3 >= w[1].3 - 1e-12, "k must be non-increasing");
        }
    }
}
